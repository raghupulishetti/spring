package com.raghu.bean;

public class Email {
	private String from;
	private String content;
	//setters and getters
	public void setFrom(String from) {
		this.from = from;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getFrom() {
		return from;
	}

	public String getContent() {
		return content;
	}

}
